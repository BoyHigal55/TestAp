 public static boolean loginAccount(String username, String password) {
        try (Connection cn = new dbConnector().getConnection()) {

            PreparedStatement checkStmt = cn.prepareStatement("SELECT COUNT(*) FROM tbl_com WHERE user = ? AND pass = ? AND status = 'Active'");
            checkStmt.setString(1, username);
            checkStmt.setString(2, password);
            ResultSet result = checkStmt.executeQuery();
            result.next();
            int count = result.getInt(1);

            return count > 0;

        } catch (SQLException er) {
            return false; 
        }
    }
public static void showTable(DefaultTableModel table) {

        try {

            ResultSet rs = new dbConnector().getData("SELECT * FROM tbl_com");

            while (rs.next()) {
                table.addRow(new String[]{rs.getString("id"), rs.getString("email"), rs.getString("pass"), rs.getString("user"), rs.getString("contact"), rs.getString("status")});
            }

        } catch (SQLException er) {
            System.out.println("Error: " + er.getMessage());
        }

    }

